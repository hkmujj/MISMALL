 // 暂时和 parse_config_attr功能相同
 // 但请不要互相使用，后期会调整
function parse_field_attr($string, $parameter = null) {
	//调用函数
	if(function_exists($string)){
		return $string($parameter);
	}
    if(0 === strpos($string,':')){
